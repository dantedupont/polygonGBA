void draw_text(u16* buffer, int x, int y, const char* text, u16 color) {
    const char* ptr = text;
    int char_x = x;
    
    while (*ptr && char_x < 240) {
        char c = *ptr++;
        
        // Only render printable ASCII characters (32-126)
        if (c >= 32 && c <= 126) {
            int char_index = c - 32; // Offset into font_data array
            const u8* char_data = font_data[char_index];
            
            // Only draw character if any part of it would be visible on screen
            if (char_x + 8 > 0 && char_x < 240) {
                // Draw 8x8 character
                for (int py = 0; py < 8 && y + py < 160; py++) {
                    u8 row = char_data[py];
                    for (int px = 0; px < 8 && char_x + px < 240 && char_x + px >= 0; px++) {
                        if (row & (0x80 >> px)) { // Test bit from left to right
                            buffer[(y + py) * 240 + char_x + px] = color;
                        }
                    }
                }
            }
        }
        char_x += 8; // Move to next character position
    }
}

// Initialize font tiles in VRAM for tile-based text rendering
void init_font_tiles() {
    // Wait for VBlank like album cover does - this might be critical for memory access timing
    while (REG_DMA3CNT & DMA_ENABLE) VBlankIntrWait();
    
    // Use character base 0 starting from tile 1 (tile 0 reserved for space/background)
    u16* fontTiles = (u16*)CHAR_BASE_ADR(0);  // Character base 0 for text
    
    // Create a blank/background tile at tile 0
    for (int i = 0; i < 16; i++) {
        fontTiles[i] = 0x0000; // All transparent pixels
    }
    
    // Convert each font character to a GBA tile format (16-color mode)
    for (int i = 0; i < 95; i++) {
        const u8* char_data = font_data[i];
        u16* tile_dest = &fontTiles[(i + 1) * 16]; // +1 to skip tile 0, each tile is 16 u16s (8x8 pixels, 4bpp)
        
        // Convert 8x8 bitmap to 4-bit tile data
        for (int row = 0; row < 8; row++) {
            u8 bitmap_row = char_data[row];
            u16 tile_row_low = 0;  // Pixels 0-3 (lower 4 pixels)
            u16 tile_row_high = 0; // Pixels 4-7 (upper 4 pixels)
            
            // Pack 8 pixels into 2 u16s (4 bits per pixel for 16-color mode)
            for (int px = 0; px < 8; px++) {
                u8 pixel_color = (bitmap_row & (0x80 >> px)) ? 1 : 0; // 1 for text, 0 for transparent
                
                if (px < 4) {
                    tile_row_low |= (pixel_color << (px * 4));
                } else {
                    tile_row_high |= (pixel_color << ((px - 4) * 4));
                }
            }
            
            tile_dest[row * 2] = tile_row_low;      // First 4 pixels of row
            tile_dest[row * 2 + 1] = tile_row_high; // Last 4 pixels of row
        }
    }
}

// Draw text using tile-based rendering on BG1
void draw_text_tiles(int tile_x, int tile_y, const char* text) {
    u16* textMap = (u16*)SCREEN_BASE_BLOCK(30); // BG1 map
    const char* ptr = text;
    int x = tile_x;
    
    // Clear the text area first (30 tiles wide for scrolling)
    for (int ty = tile_y; ty < tile_y + 1; ty++) {
        for (int tx = 0; tx < 30; tx++) {
            textMap[ty * 32 + tx] = 0; // Tile 0 (space/transparent)
        }
    }
    
    // Draw each character as a tile
    while (*ptr && x < 30) {
        char c = *ptr++;
        
        if (c >= 32 && c <= 126) {
            int tile_index = (c - 32) + 1; // +1 because tile 0 is reserved for space
            textMap[tile_y * 32 + x] = tile_index | (1 << 12); // Use palette 1 (yellow text)
        }
        x++;
    }
}